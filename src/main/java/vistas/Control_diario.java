    private void verproductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verproductosActionPerformed
        //Mostrar tabla de los prodcutos que se venden diarios   
        try{
            DefaultTableModel modelado = new DefaultTableModel();
            tablaproductos.setModel(modelado);

            //Agregar tres variables
            PreparedStatement ps = null;
            ResultSet rs = null;
            //Connection con= getConection();
            c = new Conecction().conectar();
            String sql = "select cl.rfc as RFC, ven.id_venta as ventas, pro.num_barras as codigo_de_barras, det.cantidad as cantidad, det.monto as monto from compra_venta ven INNER JOIN cliente cl ON cl.id_cliente = ven.id_cliente INNER JOIN detalles det ON ven.id_venta = det.id_venta INNER JOIN producto pro ON pro.id_producto = det.id_producto where ven.Fecha = CURRENT_DATE GROUP BY cl.rfc, ven.id_venta, pro.num_barras, det.cantidad, det.monto;";
            ps = c.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMetaD = rs.getMetaData();
            int cant_columnas = rsMetaD.getColumnCount();

            modelado.addColumn("RFC");
            modelado.addColumn("ventas");
            modelado.addColumn("codigo_de_barras");
            modelado.addColumn("cantidad");
            modelado.addColumn("monto");

            while(rs.next()){
                Object[] filas = new Object[cant_columnas]; 

                for (int i = 0; i < cant_columnas; i++){
                    filas[i] = rs.getObject(i + 1);

                }
                modelado.addRow(filas);
            }
        }catch(Exception e){
            System.out.println("e");

        }
    }//GEN-LAST:event_verproductosActionPerformed

